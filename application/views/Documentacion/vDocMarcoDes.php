                <li>Etapa de transición:<br/>Al final, verificaremos que nuestro gestor de eventos esté disponible para nuestros usuarios finales en una forma estable y funcional, sin errores, para poder dar una solución a esta problemática. También verificaremos que cumpla con las especificaciones escritas en este documento.</li>
